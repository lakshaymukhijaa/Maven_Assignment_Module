public class Calculator {
    void addition( int no1, int no2) {
        int add;
        add = no1 + no2;

        System.out.println("Addition of given numbers is: " +add);

    }

    void subtract(int no1, int no2) {
        int sub;
        sub = no1 - no2;

        System.out.println("Subtraction of given numbers is: " +sub);
    }

    void multiplication(int no1, int no2) {
        int multiply;
        multiply = no1 * no2;

        System.out.println("Multiplication of given numbers is: " +multiply);
    }

    void division(float no1, float no2) {
        float division;
        division = no1 / no2;

        System.out.println("Division of given numbers is: " +division);
    }
}